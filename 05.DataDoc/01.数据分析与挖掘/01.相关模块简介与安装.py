# 数据分析与挖掘相关模块的简介与安装:
# numpy模块:          可以高效处理数据,提供数组支持,很多模块依赖它.如: pandas, scipy, matplotlib
# pandas模块:         主要用于进行数据探索和数据分析
# scipy模块:          主要进行数值计算,同时支持矩阵运算,并提供了很多高等数据处理功能.比如: 积分, 傅里叶变换, 微分方程求解...
# matplotlib模块:     主要用于作图,解决可视化问题
# statsmodels模块:    主要用于统计分析
# gensim模块:         主要用于文本挖掘
# sklearn模块:        用于机器学习
# keras模块:          用于深度学习

# 相关模块的安装顺序与方式:
# 模块下载地址: https://www.lfd.uci.edu/~gohlke/pythonlibs/
# 1. numpy,mkl(下载安装):   numpy-1.16.2+mkl-cp36-cp36m-win_amd64.whl
# 2. pandas(网络安装):      pip install pandas
# 3. matplotlib(网络安装):  pip install matplotlib
# 4. scipy(下载安装):       scipy-1.2.1-cp36-cp36m-win_amd64.whl
# 5. statsmodels(网络安装): pip install statsmodels
# 6. gensim(网络安装):      pip install gensim


