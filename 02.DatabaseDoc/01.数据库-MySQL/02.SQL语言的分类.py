### SQL语言:结构化查询语言,是一种数据库查询和程序设计语言,用于存取数据以及查询,更新和管理关系型数据库系统

### SQL语言可以做什么?
# 数据库数据的增删改查(CRUD)
# 数据库对象的创建,修改和删除
# 用户权限/角色的授权与取消
# 事务控制

### SQL语言的分类:
# -DQL(数据查询语言): 如 select
# -DML(数据操作语言): 如 insert ,update ,delete
# -DDL(数据定义语言): 如 create ,alert ,drop
# -DCL(数据控制语言): 如 grant ,revoke
# -TCl(事务控制语言): 如 savepoint ,rollback ,set transaction ,commit







